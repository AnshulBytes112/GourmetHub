    public void clear(){
        //this functon will clear all the fields and disable save button;
        txtname.setText("");
        txtemail.setText("");
        txtphonenumber.setText("");
        txtaddress.setText("");
        txtpassword.setText("");
        btnsave.setEnabled(false);
    }
    
    public void validatefields(){// this function checks if all the fields are valid or not
        String name=txtname.getText();
        String email=txtemail.getText();
        String phonenumber=txtphonenumber.getText();
        String address=txtaddress.getText();
        String password=txtpassword.getText();
        if(!name.equals("") && email.matches(emailpattern) && phonenumber.matches(phonenumberpattern) && phonenumber.length()==10 && !address.equals("") && !password.equals("")){
            btnsave.setEnabled(true);
    } else{
            btnsave.setEnabled(false);//if condition not satisfied so button not enabled
        }}

